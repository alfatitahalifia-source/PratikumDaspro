package Jobsheet8;

import java.util.Scanner;

public class Triangle04 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Masukkan nilai N = ");
        int N = sc.nextInt();
        for (int i = 0; i <= N; i++) {
            int j = 0;
            while (j < i) {
                System.out.print("*");
                j++;
            }
        System.out.println();
        }
    }
}


